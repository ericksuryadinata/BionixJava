class Print{

    public static void main(String[] args) {
        // untuk melakukan print, normalnya kita memanggil
        System.out.println("INI PRINT DENGAN ENTER"); // output INI PRINT DENGAN ENTER
        // atau juga bisa
        System.out.print("INI PRINT TANPA ENTER"); // output INI PRINT TANPA ENTER

        // ini cuma digunakan untuk memisahkan doang dengan print selanjutnya :v
        System.out.println();

        // lah bedanya apa :v ??
        // kedua hal tersebut mempunyai fungsi yang sama, yaitu melakukan print
        // yang pertama dengan next line
        // yang kedua tidak dengan next line
        // gampang kan ??

        // jadi seumpama kalau 
        System.out.println("INI PRINT CONTOH DENGAN ENTER");
        System.out.println("INI PRINT CONTOH DENGAN ENTER");
        // output
        // INI PRINT CONTOH DENGAN ENTER
        // INI PRINT CONTOH DENGAN ENTER

        // kalau yang ini
        System.out.print("INI PRINT CONTOH TANPA ENTER");
        System.out.print("INI PRINT CONTOH TANPA ENTER");
        // output
        // // INI PRINT CONTOH TANPA ENTERINI PRINT CONTOH TANPA ENTER

        // ini cuma digunakan untuk memisahkan doang dengan print selanjutnya :v
        System.out.println();
    }
}