package pkg2.variabel;

/**
 *
 * @author erick
 */
public class Variabel {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        // ada sekitar 8 tipe data di java
        // byte, short, int, long, float, double, boolean, char
        // tapi tenang gak semuanya dipakai kok
        // tipe data yang sering dipakai int, double, float, boolean, char
        
        // biasanya kalau dalam melakukan inisialiasi variabel, kita harus melakukan assignment
        // terhadap variabel tersebut
        // contoh
        int a = 0;

        // diatas merupakan assignment variabel a dengan nilai 0, yang merupakan default assignment
        // tapi selain dengan menggunakan cara diatas, penulisannya juga bisa seperti ini
        int aa;

        // dengan cara itu sebenernya juga bisa, tetapi akan lebih aman jika kita melakukan assignment
        // terhadap variabel tersebut
        // beberapa contoh assignment

        boolean result = true;
        char capitalC = 'C';
        byte b = 100;
        short s = 10000;
        int i = 100000;
        
        // untuk sementara itu dulu tentang tipe data
        // lebih lengkap materi bisa dibaca di https://docs.oracle.com/javase/tutorial/java/nutsandbolts/datatypes.html
        // aku nyontoh dari sana kok :v
    }
    
}
